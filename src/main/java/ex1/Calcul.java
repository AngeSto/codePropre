public class Calcul {

	/**Cette méthode effectue une addition
	 * @param a paramètre a
	 * @param b paramètre b
	 * @return int
	 */
	public int addition(int a, int b){
		return a+b;
	}

	/**Cette méthode effectue une soustraction
	 * @param a paramètre a
	 * @param b paramètre b
	 * @return int
	 */
	public int soustraction(int a, int b){
		return a-b;
	}
}
